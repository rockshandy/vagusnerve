=begin
  title: hoisin shrimp & broccoli
  prep/total time: 30 min.
  yields: 4
  from: Healthy Cooking, June.July.09.66
  
  1 Tbs. cornstarch
  1/3 cup reduced-sodium chicken broth
  4-1/2 tsp. reduced-sodium soy sauce
  4-1/2 tsp. hoisin sauce
  1 tsp. sesame oil
  3 cups fresh broccoli florets
  1 Tbs. canola oil
  4 green onions, chopped
  3 garlic cloves, minced
  1 tsp. minced fresh gingerroot
  1 lb. medium shrimp, peeled and deveined
  2 cups hot cooked rice
  
  1: In a small bowl, combine cornstarch and brother until smooth. Stir in the soy sauce, hoisin sauce and sesame oil; set aside.

  2: In a large, nonstick skillet or wok, stir-fry broccoli in canola oil until crisp-tender. Add the onions, garlic, and ginger; stir-fry for 3-4 minutes or until vegetables are tender. Add shrimp; stir-fry until shrimp are warmed-cooked.

  3: Stir cornstarch mixture and add to the pan. Bring to a boil; cook and stir for 2 minutes or until thickened. Serve with rice.

  nutrition: 3/4 cup shrimp mixture with 1/2 cup rice equals 289 calories, 7 g fat (1 g saturated fat), 138 mg cholesterol, 524 mg sodium, 33 g carbohydrate, 2 g fiber, 23 g protein.

  diabetic Exchange: 3 very lean meat, 1-1/2 starch, 1 vegetable, 1 fat.
=end 

=begin
  title: rustic italian tortellini soup
  prep/total time: 
  yields: 8?
  from: Healthy Cooking, Oct.Nov.08.34
  
  12 ozs. italian sausage, casings removed
  1 medium onion, chopped
  6 garlic cloves, minced
  29 ozs reduced-sodium chicken broth
  1-3/4 cups water
  14-1/2 ozs. diced tomatoes, undrained from can
  9 ozs. refigerated cheese tortellini
  6 ozs. fresh baby spinach, coarsely chopped
  3/4 tsp. dried basil
  1/4 tsp. pepper
  1 dash red pepper flakes, crushed
  
  1: Crumble sausage into a Dutch oven; add onion. Cook and stir over medium heat until meat is no longer pink. Add garlic; cook and stir 2 minutes longer. Add the broth, water, and tomatoes. Bring to a boil.

  2: Stir in tortellini; return to a boil. Reduce heat; simmer, uncovered, for 5-8 minutes or until tortellini are tender, stirring occasionally. Add the spinach, basil, pepper and pepper flakes; cook 2-3 minutes longer or until spinach is wilted. Serve with cheese if desired.

  nutrition: 1-1/3 cups (calculated without cheese) equals 203 calories, 8g fat (2 g saturated fat), 40 mg cholesterol, 878 mg sodium. 18 g carbohydrate, 3 g fiber, 16 g protein.
  
  mention: great with herbed monkey bread!
  
  variation: we added multi-color tortellini for fun  
=end

=begin
  title: herbed monkey bread
  prep/total time: 30 min + rising and 30 min bake
  yields: 16 servings
  from: Healthly Living, Dec.Jan.09.41
  
  3 cups all-purpose flour, *
  2 Tbs. sugar
  1 tsp. salt
  1/4 oz. active dry yeast
  1-1/4 cups fat-free milk
  2 Tbs. canola oil
  4 Tbs butter, melted-divided
  1 egg
  2 Tbs. parmesan cheese, grated
  1 Tbs. sesame seeds
  1/2 tsp. garlic salt
  1/2 tsp. paprika
  1/2 tsp. dried parsley flakes
  1/2 tsp. thyme
  1/2 tsp. rosemary, crushed
  
  1: In a large mixing bowl, combine 2 cups flour, sugar, salt, and yeast. In a small saucepan, heat the milk, oil, and 2 Tbs. Butter to 120&deg;-130&deg;. Add to dry ingredients; beat just until moistened. Add egg; beat until smooth, Stir in enough remaining flour to form a soft dough.

  2: Turn onto a lightly floured surface and knead until smooth and elastic, about 6-8 minutes. Place in a bowl coated with cooking spray, turning once to coat the top. Cover and let rise in a warm place until doubled, about 1 hour.

  3: In a small bowl, combine the cheese, sesame seeds, garlic salt, paprika and herbs. Punch dough down. Divide into 32 pieces; roll each into a ball.

  4: Drizzle 2 tsp. Butter into a 10-in. Tube pan coated with cooking spray; sprinkle with a third of the cheese mixture. Top with 16 balls. Repeat layers, drizzle with remaining butter and sprinkle with remaining cheese mixture. Cover and let rise in a warm place until doubled, about 30 minutes.

  5: Bake at 350&deg; for 30-35 minutes or until golden brown. Cool for 10 minutes before removing from pan to a wire rack.

  nutrition: 2 pieces equals 150 calories, 6 g fat (2 g saturated fat), 22 mg cholesterol, 260 mg sodium, 21 g carbohydrate, 1 g fiber, 4 g protein.

  diabetic: 1-1/2 starch, 1 fat.
=end
